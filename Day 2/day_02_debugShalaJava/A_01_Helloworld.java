package day_02_debugShalaJava;

public class A_01_Helloworld
{
	public static void main(String [] args)
	{
		//Single Line comments
		System.out.println("Hello Learning from \n java by HHAPN");
		System.out.println("Hello world by Eshan \n Hello World!");
		System.out.println("I am learning Java.");
		System.out.println("It is awesome!");
		System.out.print("Hello World!\n");
		System.out.print("Not in new line\n ");
		
		
		/* MultiLine comment starts
		 The Print() Method
		There is also a print() method, which is similar to println().
		The only difference is that it does not insert a new line at the end of the output:
		 MultiLine comment ends */
		System.out.print("Hello World! ");
		System.out.print("I will print on the same line.\n");
		/*
		 Print Numbers
		You can also use the println() method to print numbers.
		However, unlike text, we don't put numbers inside double quotes:
		 */
		System.out.println(3);
		System.out.println(358);
		System.out.println(50000);
		//Forward slash(//)You can also perform mathematical calculations inside the println() method:
		System.out.println(3 + 3);
		System.out.println(2 * 5);
		
	}
}